-- QUERY023- GIS_TB01S005_CONTINENTE
--
-- Qual é o desvio padrăo da superfície dos continentes?
-- Atençăo: Colocar label (rótulo) na coluna resultante.
-- Atençăo: Năo usar a funçăo STDEV, mas sim STDEVP.
--
-- ----------------------------------------------------------------------------------------
-- Atençăo. Indicaçăo do BD a ser usado. Se for o caso, mude o nome do Banco.
-- ----------------------------------------------------------------------------------------
--
USE BDGIS;
--
-- ----------------------------------------------------------------------------------------
-- Seleçăo de todos os dados para visualizaçăo do conteúdo da tabela.
-- ----------------------------------------------------------------------------------------
--
Select stdevp(ext_km2) 'Desvio Padrăo da superfície dos continentes'
  From dbo.GIS_TB01S005_CONTINENTE;
--
-- ----------------------------------------------------------------------------------------
-- Comentários.
-- ----------------------------------------------------------------------------------------
--
-- A cláusula FROM é a única cláusula obrigatória no comando SELECT.
--
-- As Funçőes de Agregaçăo săo declaradas no comando SELECT  tal qual se faz com a declara-
-- çăo de uma coluna.
--
-- Uma funçăo de agregaçăo, ao ser exibida, terá como nome de coluna  "COLUMN NO NAME",  ou
-- seja, "Coluna Sem Nome", porque sua origem năo é da própria tabela.
--
-- Para evitar este nome de coluna, utiliza-se de um label ou rótulo de coluna.
--
-- Este comando retorna o desvio padrăo dos valores da coluna (Standard Deviation).
--
-- A funçăo STDEV é usada quando o grupo de números sendo avaliados é apenas uma amostragem
-- parcial de toda a populaçăo.
-- O denominador para dividir a soma dos desvios quadrados é N-1 , onde N é o número de ob-
-- servaçőes (uma contagem de itens no conjunto de dados).
-- Tecnicamente, subtrair o valor 1 é referido como "năo tendencioso".
--
-- já a funçăo STDEVP  é usada quando o grupo de números que está sendo avaliado  está com-
-- pleto, ou seja, é toda a populaçăo de valores.
-- Neste caso, o 1 năo é subtraído e o denominador  para dividir a soma dos desvios quadra-
-- dos é simplesmente N, o número de observaçőes  (uma contagem de itens no conjunto de da-
-- dos).
-- Tecnicamente, isso é chamado de "tendencioso".
-- Lembrando que o P em STDEVP significa "Populaçăo".
-- Como o conjunto de dados năo é uma mera amostra,  mas é constituído de TODOS  os valores
-- reais, essa funçăo de desvio padrăo pode retornar um resultado mais preciso.
