USE BDGIS;
--
-- ----------------------------------------------------------------------------------------
-- Seleçăo de todos os dados para visualizaçăo do conteúdo da tabela.
-- ----------------------------------------------------------------------------------------
--
Select checksum_agg(*) 'Checksum agg'
  From dbo.GIS_TB01S005_CONTINENTE;
--
-- ----------------------------------------------------------------------------------------
-- Comentários.
-- ----------------------------------------------------------------------------------------
--
-- A cláusula FROM é a única cláusula obrigatória no comando SELECT.
--
-- As Funçőes de Agregaçăo săo declaradas no comando SELECT  tal qual se faz com a declara-
-- çăo de uma coluna.
--
-- Uma funçăo de agregaçăo, ao ser exibida, terá como nome de coluna  "COLUMN NO NAME",  ou
-- seja, "Coluna Sem Nome", porque sua origem năo é da própria tabela.
--
-- Para evitar este nome de coluna, utiliza-se de um label ou rótulo de coluna.
--
-- Esta funçăo CHECKSUM_AGG é uma funçăo agregada que usa um único valor inteiro de  várias
-- linhas e calcula uma soma de verificaçăo agregada para cada grupo.
-- Eles podem ser usados juntos em várias colunas de soma de verificaçăo em um grupo.
--
-- Já o CHECKSUM calculava um hash para um ou mais valores  em uma única linha  e retornava
-- um valor inteiro.
