--          ja, a data e hora locais ou de Brasília).
--          Colocar uma coluna de separaçăo entre a data corrente e os demais dados, de mo-
--          do que o nome desta coluna fique em branco.
--          Em seguida, efetuar a diferença desta data  para a data 28 de novembro de 2017,
--          em ordem inversa.
--          Mostrar o resultado da diferença em dias.
--
-- ----------------------------------------------------------------------------------------
-- Atençăo. Indicaçăo do BD a ser usado. Se for o caso, mude o nome do Banco.
-- ----------------------------------------------------------------------------------------
--
USE BDGIS;
--
-- ----------------------------------------------------------------------------------------
-- Seleçăo de todos os dados para visualizaçăo do conteúdo da tabela.
-- ----------------------------------------------------------------------------------------
--
Select Getdate()    'Hora de Brasília',
       '   '        ' ',
       DateDiff (Day, '2017-11-28', Getdate()) AS 'Diferença de data em dias';
--
-- ----------------------------------------------------------------------------------------
-- Comentários.
-- ----------------------------------------------------------------------------------------
--
-- A funçăo DateDiff retorna um valor que corresponde ŕ contagem dos limites  das datas es-
-- pecificadas entre a data inicial e a data final.
--
-- O valor inicial é subtraído do valor final da data.
--
-- O resultado é dado em dias, porque foi especificado DAY  como primeiro parâmetro da fun-
-- çăo.
--
-- Reparar que năo é necessário selecionar nenhum dado de nenhuma tabela para exibir a data
-- e hora correntes e nem para fazer os cálculos.
--
-- A funçăo GETDATE() recolhe a data e hora correntes (ou seja, a data e hora locais ou da-
-- ta e hora de Brasília).
