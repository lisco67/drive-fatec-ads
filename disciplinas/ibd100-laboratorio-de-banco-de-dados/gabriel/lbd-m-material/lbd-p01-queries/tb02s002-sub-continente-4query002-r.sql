-- QUERY002- GIS_TB02S002_SUB_CONTINENTE
--
-- Selecionar todos os subcontinentes, explicitando as colunas no comando.
--
-- ----------------------------------------------------------------------------------------
-- Atençăo. Indicaçăo do BD a ser usado. Se for o caso, mude o nome do Banco.
-- ----------------------------------------------------------------------------------------
