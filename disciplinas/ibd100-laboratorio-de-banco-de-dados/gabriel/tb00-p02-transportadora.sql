-- P02- Transportadora
--
-- GIS TRANSPORTUDO é uma transportadora  que possui uma matriz no Brasil,  com várias fi-
-- liais distribuídas por diversos estados do país e também em vários países.
-- A GIS TRANSPORTUDO retira, transporta  e entrega mercadorias para todo o Brasil  e paí-
-- ses da América do Sul e América Central.
-- A transportadora conta com uma frota diversificada de caminhőes, VANs e VUCs.
-- Os veículos săo diferenciados para transporte de mercadorias, como VUC,  TOCO (semi-pe-
-- sado), Truck (pesado), Cavalo Mecânico (extra-pesado),  Cavalo Mecânico Trucado  ou LS,
-- Bitrem (ou Treminhăo), Rodotrem e Guincho (para reboque de outros veículos).
-- Os veículos podem ser de marcas, cores e ano de fabricaçăo diferentes.  A administraçăo
-- conta com um relatório que mostra os veículos por fabricante.
-- A frota é renovada a cada 5 anos, sendo que a cada ano săo adquiridos novos veículos.
-- Veículos que completam 5 anos de utilizaçăo săo vendidos ou alugados. Todas estas tran-
-- saçőes ficam registradas na empresa.
-- Todos os motoristas dos veículos, clientes, mercadorias, trajetos, oficinas e funcioná-
-- rios săo devidamente cadastrados.
-- Todo e qualquer transporte de carga é feito com dois motoristas.
-- Cada motorista possui uma ficha cadastral e ficha médica completas,  atualizadas sempre
-- que necessário.
-- Cada motorista é acompanhado constantemente.
-- A empresa mantém um mapeamento de todas as praças de pedágios, com os valores atualiza-
-- dos.
-- O frete é tabelado e a empresa respeita esta tabela.
-- Todas as saídas e chegadas săo programadas.
-- Toda retirada e entrega de mercadorias săo registradas.
-- Toda carga é registrada em manifestos, onde consta o valor da mesma.
-- Cada viagem deve ser autorizada por um funcionário supervisor,  devidamente identifica-
-- do. A autorizaçăo contém a data, hora, local e id do supervisor.
-- Nenhum motorista é escalado como motorista principal  para duas ou mais  viagens longas
-- sucessivas. Entende-se por viagem longa aquela viagem superior a 400 Km.
-- Um motorista năo pode ficar como motorista primário por mais de 4 (quatro) horas conse-
-- cutivas. Atingido este tempo,  o motorista primário e o motorista secundário devem tro-
-- car de posiçăo. Esta mudança é sempre registrada.
-- Os eventos de uma viagem săo registrados  no que é chamado Diário de Bordo.  Cada ocor-
-- ręncia possui data, hora, identificaçăo do veículo, da viagem e do motorista.
-- As folgas dos motoristas săo controladas.
-- Os motoristas năo recebem salário,  mas săo pagos por quilometragem rodada,  conforme a
-- carga que transportam. Quanto maior o peso da carga, maior é o valor pago  por quilôme-
-- tro rodado.
-- Na volta da viagem, năo há pagamento.
-- Tantos as refeiçőes dos motoristas como o abastecimento do veículo săo reembolsados pe-
-- la transportadora, desde que sejam devidamente documentadas e registradas.
-- A empresa realiza periodicamente uma estatística dos gastos com refeiçőes  e dos gastos
-- com combustíveis e manutençăo dos veículos.
-- Eventuais gastos com manutençăo do veículo durante a viagem também săo reembolsados.
-- Cada veículo da frota possui um histórico de manutençăo,  que é realizada periodicamen-
-- te,  e em funçăo de necessidades específicas,  em oficinas cadastradas junto  ŕ  trans-
-- portadora.
-- Estas oficinas  contam com funcionários habilitados  e especializados,  devidamente ca-
-- dastrados.
